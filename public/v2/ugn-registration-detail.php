                              <p>
                                 The Indian Succession Act recognizes two types of wills, privileged and unprivileged wills. Privileged wills are those that can be made my members of the armed forces employed in an expedition or engaged in actual warfare and can be made in oral form as well. A relaxation of formalities has been envisaged for them considering the inherent dangers and possibility of sudden death coupled with the lack of time and means to deliberately frame written wills. Unprivileged wills are the wills that can be created by every person other than those who can create a privileged will. For a unprivileged will to be executed it is firstly necessary that it be in writing. The law requires no particular form except that the words must be intelligible and clear. The testator is required to sign or affix his mark on the will or have it signed by another person in his presence or on his direction. Two or more witnesses to the fact of the testator’s assent being expressed by placing of a mark on the will are required.
                              </p>
                              <p>The other types of will are:</p>
                              <ul>
                                 <li>
                                    <p class="font-weight-bold"> Oral or Nuncupative Will</p>
                                    <p>An Oral or Nuncupative Will is a will which has been orally declared by the testator making it in the presence of witnesses. It may be noted that the Indian Succession Act does not provide for making of oral or nuncupative will except by the soldiers, mariners and airmen (as provided under Sections 65 and 66).</p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">Mutual (or reciprocal) Will</P>
                                    <p > Mutual will is a will under which two testators confer reciprocal benefits upon each other.</p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">Joint Will </p>
                                    <p> A joint will is a will whereby two (or more) testators agree to make a conjoint will. Very common example of joint will is the will made by a husband and wife disposing of their property under one will jointly made by both. </p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">Conditional or Contingent Will</p>
                                    <p>It is a will which is expressed to take effect only in the event of the happening or non happening of a certain conditions or contingency specified therein.</p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">Holograph Will</p>
                                    <p>It is a will written by the testator in his own handwriting.</p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">In officious Will</p>
                                    <p>It is a will which is not consonant with the testator's natural love, affection and moral duty. Under an in officious will a testator bequeaths all his property to a stranger or a third party instead of to his children, his wife (or her husband), and other near relatives.</p>
                                 </li>
                                 <li>
                                    <p class="font-weight-bold">Duplicate Will</p>
                                    <p> It is the duplicate of the original will. The duplicate will can be proved and probated if the loss of the original will is proved.</p>
                                 </li>
                              </ul>
