                              <p>When an individual dies without making a will he is said to have died intestate and in that case his property will be inherited by his heirs in accordance with the law of succession as applicable to that person. Succession to the property of Hindus is governed by the provisions of the Hindu Succession Act, 1956. Succession to property of Muslims is governed by the Muslim Law. A person other than Hindus and 3 Muhammedans viz Jain, Sikh or Buddhist is governed by the Indian Succession Act, 1925. 6. GENERAL TERMS RELATING TO A WILL</p>
                              <p class="font-weight-bold">Applicable Provisions</p>
