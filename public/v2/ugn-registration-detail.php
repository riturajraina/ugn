                              <p class="font-weight-bold">Assumptions</p>
                              <ul>
                                 <li>The testator is an adult (above 18 years), Indian citizen, having permanent and correspondence address in India.</li>
                                 <li>The testator is acting out of his / her free wish to prepare the Will.</li>
                                 <li>The immovable properties / assets stated in the Will are situated in India and in the testator’s own name.</li>
                                 <li>The immovable properties / assets in the Will are in the testator’s own name and not jointly held, and if jointly held then only his share can be mentioned in the Will.</li>
                                 <li>In case of ancestral property / assets, those have rightfully devolved upon the testator as his rightful legal share in the ancestral property / assets and are situated in India.</li>
                                 <li>The testator, upon his death, wishes to dispose and distribute his property and assets situated in India and as set out in the Will.</li>
