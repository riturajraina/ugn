					<div id="accordion6">
						  <p>Passport  is a very valuable document. Thus, in order to ensure that it does not go in  wrong hands, police verification is required for issuance of passport.</p>
						  <p>There  are 2 modes of police verification:</p>
						  <ol>
							<li>Police Verification is required on a  Post-Passport Issuance basis.</li>
							<li>Police Verification is required on Pre-Passport  Issuance basis.</li>
							<li>Depending on whether an application is for  issue of fresh passport or re-issue of passport, Passport Office (PO) shall  decide whether Police Verification is required for issuance of Passport to you.  In most cases, of issue of fresh passport, pre-police verification would be  required, exception being Government servants on submission of 'Identity  Certificate' as per Annexure 'A', or minors whose parent(s) hold valid passport  etc. If your age is 18 or above, you must submit Aadhaar Card/ letter/  e-Aadhaar containing 12-digit Aadhaar number/ 28-digit Aadhaar Enrolment ID  printed on Aadhaar Enrolment slip issued by Unique Identification Authority Of  India (UIDAI), and any two documents from&nbsp;<a href="https://portal2.passportindia.gov.in/AppOnlineProject/popuponline/AttachmentAdvisorSub?subDocID=7003&minorFlag=1" target="_blank">List of Acceptable  Documents</a>&nbsp;along with Standard Affidavit (as per Annexure  &quot;E&quot;).&nbsp;<br>
							  If your age is below 18 years, you must submit Aadhaar Card/ letter/ e-Aadhaar  containing 12-digit Aadhaar number/ 28-digit Aadhaar Enrolment ID printed on  Aadhaar Enrolment slip issued by Unique Identification Authority Of India  (UIDAI), and any one document from&nbsp;<a href="https://portal2.passportindia.gov.in/AppOnlineProject/popuponline/AttachmentAdvisorSub?subDocID=7003&minorFlag=2" target="_blank">List of Acceptable  Documents</a>&nbsp;along with Standard Affidavit (as per Annexure  &quot;E&quot;). The general rules, regulations and procedures pertaining to  issue of passports as stipulated in Passports Act and Passports Rule would  apply. In most cases of re-issue, depending on records available in the  Passport Seva system, police verification may not be required or only post  police verification may be required, exceptions being re-issue of passport in  lieu of lost passport or complete change of name.</li>
						  </ol>
