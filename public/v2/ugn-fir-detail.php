										<h6>Bailable offence</h6>
										<p class="text-justify">Bailable offence means an offence which is shown as bailable in the First Schedule, <strong>Section 2(a)</strong> of the<strong> Code of Criminal Procedure, 1973</strong> or which is made bailable by any other law, for the time being in force. Bailable consists of two parts, first relates to offences under<strong> Indian Penal code</strong> and, Second one relates to offences under <strong> other laws</strong>. The second part says that the offence punishable with imprisonment for less than three years or fine only shall be bailable. Under Bailable offence a bail can be claimed as a matter of right and constitutes less serious offence. Bail means the guarantee or sum of money demanded by a Law Court. It is paid by arrested person on a criminal charges, temporarily released until which time he is allowed to go free.</p>

										<ul>
											<li>Examples being a member of an unlawful Assembly.</li>
											<li>Rioting, armed with deadly weapon.</li>
											<li>Public servant disobeying a direction of the law with intent to cause injury to any person.</li>
											<li>Wearing Garb or carrying token used by public servant with fraudulent intents.</li>
											<li>Bribery in relation to elections.</li>
											<li>False statement in connection with elections.</li>
											<li>Refusing oath when duly required to take oath by a public servant.</li>
											<li>Obstructing public Servant in discharge of his public functions.</li>
											<li>Giving or fabricating false evidence in a judicial proceeding.</li>
											<li>Selling any food or drink as food and drink, knowing the same to be noxious.</li>
											<li>Causing a disturbance to an assembly engaged in religious worship.</li>
										</ul>
										<h6>Non-Bailable Offence</h6>
										<p class="text-justify">Any other offence, apart from those offences mentioned as bailable, is considered to be non-bailable. In other words, offences punishable with imprisonment for three years or more are usually considered as non-bailable.</p>

										<p class="text-justify">The term ‘non-bailable offence’ does not means that an accused person cannot get bail under any circumstances. <strong>Section 437</strong> of the <strong>Code of Criminal Procedure</strong> provides the provisions for bail in non-bailable offences. Except on reasonable grounds that the offence committed by accused is punishable with death or imprisonment for life. As per <strong>Section 50(2)</strong> of the <strong>Code of Criminal Procedure</strong> that where a police officer arrests without warrant any person other than a person accused of a non-bailable offence. Person arrested informed that he can get bail and arrange sureties on this behalf. Examples are;</p>

										<ul>
											<li>Murder (S.302) IPC</li>
											<li>Dowry Death (S.304-B) IPC</li>
											<li>Attempt to murder (S.307) IPC</li>
											<li>Voluntary causing grievous hurt. (S.326) IPC</li>
											<li>Kidnapping (S. 363) IPC</li>
											<li>Rape (S. 376) etc.</li>
										</ul>
										<h6>Difference between Bailable offence and Non-Bailable offence</h6>
										<p>Following points differentiate Bailable offence from Non-bailable offence :</p>
										<ol>
											<li>In the case of bailable, it’s binding upon the investigating officer to grant bail. While in case of a non-bailable offence the police do not grant bail. The decision to grant bail is taken by a <strong>Judicial Magistrate/Judge</strong> only.</li>

											<li>In the case of a bailable, if the accused produces proper surety after his arrest, and fulfils other conditions, its binding upon the Investigating officer to release him. While in the case of a non-bailable offence, the Investigating Officer must present the accused before the <strong>Judicial Magistrate/Judge</strong> concerned within <strong>24 hours</strong> of the arrest. At that time, the accused has a right to apply for bail himself or through his representative /lawyer.</li>

										</ol>
										<p>Similarly, if the accused has subjected to any misbehaviour by police after arrest, he has an opportunity to complain against the same before the <strong>Judicial magistrate</strong>. </p>
