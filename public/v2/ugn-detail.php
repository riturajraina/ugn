						<h5 class="text-center mb-3">History of PAN in India</h5>
						<p>Earlier, before the concept of PAN was introduced, there was a GIR number assigned to taxpayers which was basically a manual system and as such was unique only within a ward or under a particular assessing officer. However, the number was not unique at the country level. In the year 1972, the concept of PAN was rolled out by the Indian government and was made statutory under section 139A of the Income Tax Act, 1961.</p>
						
						<p>The GIR number was allotted by the Assessing Officer to a tax payer and it included the Assessing Officer’s information as well.</p>
						<p>Another problem with the GIR system was that it was not a unique number. A GIR was unique only in that particular ward/circle, resulting in errors or the chances of miscalculations during tax assessment.</p>
						<p>The PAN card in its present form was first introduced in 1972, as a way to overcome these problems. Initially a voluntary process, it was made mandatory for all tax paying individuals in the country in 1976.</p>
						<p>The initial PAN number allotments were made manually, and to avoid duplication, each ward/circle received a certain set of numbers. This series was abandoned in the year 1995 due to a host of problems associated with it.</p>
						<p class="font-weight-bold">In spite of these changes, the first avatar of the PAN card met with some difficulties, as mentioned below:</p>
						<ul>
						<li>There was no database to maintain records of the PAN number allotted, with very limited information being recorded.</li>
						<li>There was no centralised authority who issued the PAN cards, resulting in the possibility that different centres could allot the same number to different individuals in the country. The PAN card number was not a permanent one as the number changed depending on the holder’s address.</li>
					</ul>

